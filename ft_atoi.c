int	ft_atoi(const char *str)
{
	int	i;
	int	min;

	i = 0;
	min = 0;
	while (*str == ' ' || *str == '\n' || *str == '\t'
		|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			min++;
		str++;
	}
	while ('0' <= *str && *str <= '9')
	{
		i = i * 10;
		i = i + *str - '0';
		str++;
	}
	if (min != (min / 2) * 2)
		i = -i;
	return (i);
}
