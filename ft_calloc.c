#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*dst;

	dst = malloc(count * size);
	if (dst != 0)
	{
		ft_bzero(dst, (count * size));
		return (dst);
	}
	return (NULL);
}
